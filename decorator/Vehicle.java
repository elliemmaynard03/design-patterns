package decorator;
/*
 * creates an arraylist of strings called lines
 */
public class Vehicle {
    protected ArrayList<String> lines;

    /*
     * method uses arraylist lines 
     * @return lines
     * @param lines
     */
    public Vehicle(ArrayList<String> lines) {
       return lines;
    }
    /*
     * creates a toString of the arraylist lines
     * @return lines
     */
    public String toString() {
        for (String line : lines)
        {
           return lines;
        }
    }
}
